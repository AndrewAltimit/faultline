/**
 * Check if a scenario's regions match the US macro-region layout.
 * @param {object} regions - scenario.map.regions
 * @returns {boolean}
 */
export function isUSScenario(regions) {
  const regionIds = Object.keys(regions);
  const usIds = Object.keys(US_REGIONS);
  // Match if at least 5 of the 8 US region IDs are present.
  const matches = regionIds.filter((rid) => usIds.includes(rid));
  return matches.length >= 5;
}
